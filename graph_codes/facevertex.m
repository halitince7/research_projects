R := GF(2);


function FVIM(G)
F := Faces(G);
V := Vertices(G);
FM :=ZeroMatrix(R, #F, #V); 

for i:=1 to #F do
    for j:=1 to #V do
        
        for e in F[i] do
            if InitialVertex(e) eq V[j] then
               FM[i][j] := R!1;
            end if;
        end for;

    end for;
end for;


return FM;
end function;


