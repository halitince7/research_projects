function FVIM(G)
F := Faces(G);
V := Vertices(G);
FM :=ZeroMatrix(GF(2), #F, #V); 
for i:=1 to #F do
    for j:=1 to #V do
        
        for e in F[i] do
            if InitialVertex(e) eq V[j] then
               FM[i][j] := GF(2)!1;
            end if;
        end for;
   
    end for;
end for;
return FM;
end function;


